ALTER PROCEDURE sp_PAGARCARRITO(@param_PROVINCIA VARCHAR(50), 
							  @param_CANTON VARCHAR(50),
							  @param_DETALLE VARCHAR(50),
							  @param_TIPO VARCHAR(50),
							  @param_NUMERO VARCHAR(50),
							  @param_CVV INT,
							  @param_FECHA VARCHAR(100),
							  @param_CEDULA INT
							  )
AS
BEGIN
	IF EXISTS (SELECT TC.TIPO,TC.NUMERO,TC.CODIGOSEGURIDAD,TC.FECHAVENCIMIENTO FROM USUARIO.tb_TARJETACREDITO TC WHERE TC.NUMERO = @param_NUMERO AND TC.CODIGOSEGURIDAD = @param_CVV AND TC.TIPO = @param_TIPO AND TC.FECHAVENCIMIENTO = @param_FECHA)
	BEGIN
		SELECT 'Ya exite la tarjeta'
	END
	ELSE
	BEGIN
		INSERT USUARIO.tb_TARJETACREDITO(
		[NUMERO]
		, [CODIGOSEGURIDAD]
		, [TIPO]
		, [FECHAVENCIMIENTO]
		)VALUES(
		@param_NUMERO
		,@param_CVV
		,@param_TIPO
		,@param_FECHA
		)

		DECLARE @CREDIT INT = (SELECT TOP 1 IDCREDIT FROM USUARIO.tb_TARJETACREDITO ORDER BY IDCREDIT DESC)
		INSERT INTO USUARIO.tb_USUARIO_TARJETACREDITO(
		[CEDULA]
		, [IDCREDIT]
		)VALUES(
		@param_CEDULA
		,@CREDIT
		)
	END
	IF EXISTS(SELECT D.PROVINCIA, D.CIUDAD, D.DETALLE FROM FACTURA.tb_DIRECCION D  WHERE D.PROVINCIA = @param_PROVINCIA AND D.CIUDAD = @param_CANTON AND D.DETALLE = @param_DETALLE)
	BEGIN
		SELECT 'Ya existe la direccion'
	END
	ELSE
	BEGIN
		INSERT INTO FACTURA.tb_DIRECCION(
		[PROVINCIA]
		, [CIUDAD]
		, [DETALLE]
		)VALUES(
		@param_PROVINCIA
		,@param_CANTON
		,@param_DETALLE
		)

		DECLARE @DIRECCION INT = (SELECT TOP 1 ID FROM FACTURA.tb_DIRECCION ORDER BY ID DESC)
		INSERT INTO FACTURA.tb_COMPRA(
		[IDCLIENTE]
		, [FECHA]
		, [ID_DIRECCION]
		)VALUES(
		@param_CEDULA
		,CONVERT(VARCHAR(50),GETDATE(),110)
		,@DIRECCION
		)
	END
	DECLARE @CANT INT = (SELECT COUNT(IDP) FROM PRODUCTO.tb_CARRITO WHERE IDU = @param_CEDULA)
	WHILE ( @CANT != 0)
	BEGIN
		DECLARE @IDCOMPRA INT = (SELECT TOP 1 IDCOMPRA FROM FACTURA.tb_COMPRA ORDER BY IDCOMPRA DESC)
		DECLARE @CANTIDAD INT = (SELECT TOP 1 C.CANTIDAD FROM PRODUCTO.tb_CARRITO C WHERE C.IDU = @param_CEDULA ORDER BY C.IDP ASC)
		DECLARE @IDP INT = (SELECT TOP 1 C.IDP FROM PRODUCTO.tb_CARRITO C WHERE C.IDU = @param_CEDULA ORDER BY C.IDP ASC)
		DECLARE @PRECIO INT = (SELECT TOP 1 P.PRECIO FROM PRODUCTO.tb_PRODUCTO P WHERE P.IDP = @IDP)
		DECLARE @MONTO INT = @PRECIO * @CANTIDAD
		INSERT INTO FACTURA.tb_DETALLE_FACTURA(
		[IDCOMPRA]
		, [IDPRODUCTO]
		, [CANTIDAD]
		, [MONTO]
		)VALUES(
		@IDCOMPRA
		,@IDP
		,@CANTIDAD
		,@MONTO
		)
		DELETE FROM PRODUCTO.tb_CARRITO
		WHERE IDP = @IDP AND IDU = @param_CEDULA
		SET @CANT = (SELECT COUNT(IDP) FROM PRODUCTO.tb_CARRITO WHERE IDU = @param_CEDULA)
	END
END