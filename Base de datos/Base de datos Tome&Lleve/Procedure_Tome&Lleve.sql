ALTER PROCEDURE sp_PAGAR(@param_PROVINCIA VARCHAR(50), 
							  @param_CANTON VARCHAR(50),
							  @param_DETALLE VARCHAR(50),
							  @param_TIPO VARCHAR(50),
							  @param_NUMERO VARCHAR(50),
							  @param_CVV INT,
							  @param_FECHA VARCHAR(100),
							  @param_CEDULA INT,
							  @param_IDP INT,
							  @param_CANTIDAD INT,
							  @param_MONTO INT)
AS
BEGIN

	INSERT USUARIO.tb_TARJETACREDITO(
	[NUMERO]
	, [CODIGOSEGURIDAD]
	, [TIPO]
	, [FECHAVENCIMIENTO]
	)VALUES(
	@param_NUMERO
	,@param_CVV
	,@param_TIPO
	,@param_FECHA
	)

	DECLARE @TARJETA INT = (SELECT TOP 1 IDCREDIT FROM USUARIO.tb_TARJETACREDITO ORDER BY IDCREDIT DESC)
	INSERT INTO USUARIO.tb_USUARIO_TARJETACREDITO(
	[CEDULA]
	, [IDCREDIT]
	)VALUES(
	@param_CEDULA
	,@TARJETA
	)

	INSERT INTO FACTURA.tb_DIRECCION(
	[PROVINCIA]
	, [CIUDAD]
	, [DETALLE]
	)VALUES(
	@param_PROVINCIA
	,@param_CANTON
	,@param_DETALLE
	)

	DECLARE @DIRECCION INT = (SELECT TOP 1 ID FROM FACTURA.tb_DIRECCION ORDER BY ID DESC)
	INSERT INTO FACTURA.tb_COMPRA(
	[IDCLIENTE]
	, [FECHA]
	, [ID_DIRECCION]
	,[IDCREDIT]
	)VALUES(
	@param_CEDULA
	,CONVERT(VARCHAR(50),GETDATE(),110)
	,@DIRECCION
	,@TARJETA
	)

	DECLARE @IDCOMPRA INT = (SELECT TOP 1 IDCOMPRA FROM FACTURA.tb_COMPRA ORDER BY IDCOMPRA DESC)
	INSERT INTO FACTURA.tb_DETALLE_FACTURA(
	[IDCOMPRA]
	, [IDPRODUCTO]
	, [CANTIDAD]
	, [MONTO]
	)VALUES(
	@IDCOMPRA
	,@param_IDP
	,@param_CANTIDAD
	,@param_MONTO
	)
END

