CREATE PROCEDURE sp_iniciarSesion(@param_ALIAS VARCHAR(50), @param_CONTRASENNA VARCHAR(50))
AS 
BEGIN
	SELECT
	U.CEDULA
	,U.ALIAS
	,U.TIPO
	FROM USUARIO.tb_USUARIO U
	WHERE U.ALIAS = @param_ALIAS AND U.CONTRASENNA = @param_CONTRASENNA
END
GO
